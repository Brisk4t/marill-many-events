package com.example.marill_many_events;

public class User {
    public String name;
    public String email;
    public int phone;

    public User() {
        // Default constructor
    }

    public User(String name, String email, int phone) {
        // Initialize user profile
        this.name = name;
        this.email = email;
        this.phone = phone;
    }
}
